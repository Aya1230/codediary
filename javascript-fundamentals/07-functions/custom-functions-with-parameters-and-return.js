// Define a function called add that takes two parameters: a and b
function add(a, b) {
  // Return the result of adding a and b
  return a + b;
}

// Call the function with 4 and 5, and store the result in a variable
let sum = add(4, 5); // ✅ sum now holds 9

// Log the returned result to the console
console.log(sum); // ✅ Output: 9
